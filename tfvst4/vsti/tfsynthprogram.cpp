#include "tfvsti.hpp"

void eTfSynthProgram::loadDefault(int i)
{
	// Default Program Values
	memcpy(params, TF_DEFAULTPROG, TF_PARAM_COUNT * sizeof(eF32));

	char str[10];
	_itoa(i, str, 10);
	strcpy(name, "Prog ");
	strcat(name, str);
}

